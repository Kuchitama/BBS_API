CREATE TABLE THREAD (
  ID BIGINT(20) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  TITLE VARCHAR(200) NOT NULL,
  TAGS VARCHAR(600) NOT NULL,
  CREATED_BY BIGINT NOT NULL,
  CREATED_AT TIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP NOT NULL
);

